//Javascript is a dynamc language.
 
//In dynamic language the type of a variable can change at run time.

// typeof  Is the operator used to check the type of a variable in the console.

let firstName = 'Iqra';
console.log(firstName);

// the typeof firstName is a string but in a dynamic language the type can change at run time.

// Since variables are temporarily locations for storing data we can change the typeof a variable easily.

// We assign the firstName variable a new value in the console let's say 1.

console.log(typeof firstName);

//  "string"  This is the output in the console.

 firstName = 1;
 console.log(typeof firstName);

//  "number" This is the output in the console.

 // This is what we call a dynamic language.


